    private double[] resolver(double[][] m, double[] r) {
        
        int n1 =this.getN1();
        for (int i = 0; i < 10; i++) {
            double d,c= 0;
            d = m[i][i];
            txtprocedimiento.append(Double.toString(1/d) + "*fila" + (i+1) + "\n");
            for (int j = 0; j < n1; j++) {
                m[i][j] = ((m[i][j]) / d);
            }
            r[i] = ((r[i]) /d);
            
            for (int j = 0; j < n1; j++) {
                for (int k = 0; k < n1; k++) {
                    txtprocedimiento.append(Double.toString(m[j][k]) + "\t");
                }
                txtprocedimiento.append("|\t" + Double.toString(r[j]) + "\n");
            }
            txtprocedimiento.append("\n\n");
            
            for (int x = 0; x < n1; x++) {
                if (i != x) {
                    c = m[x][i];
                    txtprocedimiento.append("-" +Double.toString (c) + " *fila " +(i+1) + "+ fila" + (x+1) + "\n");
                    for (int y = 0; y < n1; y++) {
                        m[x][y] = m[x][y] - c * m[i][y];
                    }
                    r[x] = r[x] - c * r[i];
                    
                    for (int j = 0; j < n1; j++) {
                        for (int k = 0; k < n1; k++) {
                            txtprocedimiento.append(Double.toString (m[j][k])+ "\t"); 
                        }
                        txtprocedimiento.append("|\t" + Double.toString(r[j])+ "\n");
                    }
                    txtprocedimiento.append("\n\n");
                }
            }
        }
        return r;
    }
