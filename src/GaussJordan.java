    private void btngenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngenerarActionPerformed
        // TODO add your handling code here:
        try {
            this.setN1(Integer.parseInt(txtvalor.getText()));
            n1 = this.getN1();
             
             if (n1<=0) {
                throw new Exception ("El valor es inválido");
            }
            Object columna[] = new Object [n1+1];  
            
            for (int i = 0; i < n1+1; i++) {
                if (i<n1) {
                    columna[i] = "X" + (i+1);
                }else{
                    columna[i] = "D";
                }
            }
            modelo = new DefaultTableModel(columna,n1);
            jTable.setModel(modelo);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btngenerarActionPerformed

    private void btnresolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnresolverActionPerformed
        // TODO add your handling code here:
        try {
            int n1 = this.getN1();
            double m[][] = new double [n1][n1];
            double r[]= new double [n1];
            
            for (int i = 0; i < n1; i++) {
                for (int j = 0; j < n1; j++) {
                    m[i][j] = Double.parseDouble(String.valueOf(jTable.getValueAt(i,j)));  
                }
                r[i] = Double.parseDouble(String.valueOf(jTable.getValueAt(i,n1)));
            }
            r = this.resolver(m,r); 
            
            for (int i = 0; i < n1; i++) {
                txtprocedimiento.append("X" + (i+1) + " = " + r[i] + "\n" ); 
            }
                        
        } catch (Exception e) { 
            System.out.println(e);
        }
    }//GEN-LAST:event_btnresolverActionPerformed

